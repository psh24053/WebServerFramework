package com.shntec.server.model;

public class Activity {

	private int act_id;
	private String act_content;
	private String act_date;
	private String act_name;
	private String act_location_des;
	private String act_location_gps;
	private String act_img;
	
	public int getAct_id() {
		return act_id;
	}
	public void setAct_id(int act_id) {
		this.act_id = act_id;
	}
	public String getAct_content() {
		return act_content;
	}
	public void setAct_content(String act_content) {
		this.act_content = act_content;
	}
	public String getAct_date() {
		return act_date;
	}
	public void setAct_date(String act_date) {
		this.act_date = act_date;
	}
	public String getAct_name() {
		return act_name;
	}
	public void setAct_name(String act_name) {
		this.act_name = act_name;
	}
	public String getAct_location_des() {
		return act_location_des;
	}
	public void setAct_location_des(String act_location_des) {
		this.act_location_des = act_location_des;
	}
	public String getAct_location_gps() {
		return act_location_gps;
	}
	public void setAct_location_gps(String act_location_gps) {
		this.act_location_gps = act_location_gps;
	}
	public String getAct_img() {
		return act_img;
	}
	public void setAct_img(String act_img) {
		this.act_img = act_img;
	}
	
	
	
}
